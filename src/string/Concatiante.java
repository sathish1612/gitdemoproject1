package string;

public class Concatiante {
public static void main(String[] args) {
	String s1 ="java";
	String s2 = " manual";
	String s3 = " sql";
	String s4 = " selenium";
	String s5 = " project";
	String s = s1.concat(",").concat(s2).concat(",").concat(s3).concat(",").concat(s4).concat(",").concat(s5);
	System.out.println(s);
	
}
}
